#!/usr/bin/env python3
"""Validate post receipt JSON schema and repo-local artifact paths.

By default, --check-paths only validates artifacts that should exist in the
repo (figures, papers, configs, code). Artifacts that are *outputs* of
reproduction (blog_artifacts/*, physics/*, metrics/*, etc.) are skipped
because a reader generates those by running the receipt commands.

Pass --check-outputs to also verify output artifacts against a data root
(useful on the training cluster where /data has the original run outputs).
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


# Artifact path prefixes that are reproduction *outputs*, not repo inputs.
# These are generated by running the receipt commands, not shipped in git.
_OUTPUT_PREFIXES = (
  "blog_artifacts/",
  "physics/",
  "miniseries/",
  "metrics/",
  "experiments/",
  "campaign_runs/",
)


def _is_output_artifact(path: str) -> bool:
  return any(path.startswith(pfx) for pfx in _OUTPUT_PREFIXES)


def _load_json(path: Path) -> dict[str, Any]:
  obj = json.loads(path.read_text(encoding="utf-8"))
  if not isinstance(obj, dict):
    raise TypeError(f"receipt file must be a JSON object: {path}")
  return obj


def _map_path(raw: str, repo_root: Path, data_root: Path) -> Path:
  # Output artifacts resolve under the data root.
  if _is_output_artifact(raw):
    return data_root / raw
  if raw.startswith("experiments/"):
    return data_root / raw[len("experiments/"):]
  if raw.startswith("/data/"):
    return data_root / raw[len("/data/"):]
  if raw.startswith("/"):
    return Path(raw)
  # Repo-local: papers, static/figures, configs, code, etc.
  direct = (repo_root / raw).resolve()
  if direct.exists():
    return direct
  for parent in repo_root.parents:
    candidate = (parent / raw).resolve()
    if candidate.exists():
      return candidate
  # Monorepo compatibility: external `nmoe/...` maps to the internal mirror.
  if raw.startswith("nmoe/"):
    return (repo_root / "community" / raw).resolve()
  return direct


def _verify_receipt(
  path: Path,
  repo_root: Path,
  data_root: Path,
  check_paths: bool,
  check_outputs: bool,
) -> list[str]:
  errs: list[str] = []
  obj = _load_json(path)
  for key in ("post", "title", "evidence_tier", "artifacts"):
    if key not in obj:
      errs.append(f"{path}: missing key {key!r}")
  if "commands" not in obj and "repro_commands" not in obj:
    errs.append(f"{path}: missing 'commands' or 'repro_commands'")

  artifacts = obj.get("artifacts", [])
  if not isinstance(artifacts, list):
    errs.append(f"{path}: 'artifacts' must be a list")
    artifacts = []
  for cmd_key in ("commands", "repro_commands", "provenance_commands"):
    commands = obj.get(cmd_key)
    if commands is None:
      continue
    if not isinstance(commands, list) or not all(isinstance(c, str) and c.strip() for c in commands):
      errs.append(f"{path}: '{cmd_key}' must be a list of non-empty strings")

  for idx, art in enumerate(artifacts):
    if not isinstance(art, dict):
      errs.append(f"{path}: artifacts[{idx}] must be an object")
      continue
    p = art.get("path")
    if not isinstance(p, str) or not p.strip():
      errs.append(f"{path}: artifacts[{idx}] missing non-empty 'path'")
      continue
    if not check_paths:
      continue
    # Skip output artifacts unless --check-outputs is set.
    if _is_output_artifact(p) and not check_outputs:
      continue
    mapped = _map_path(p, repo_root=repo_root, data_root=data_root)
    if not mapped.exists():
      errs.append(f"{path}: missing artifact path {p} (mapped: {mapped})")
  return errs


def main() -> int:
  ap = argparse.ArgumentParser(
    description="Validate post receipts JSON schema and repo-local artifact paths.",
  )
  ap.add_argument("--repo-root", default=".", help="Repo root (default: cwd)")
  ap.add_argument(
    "--data-root",
    default="/data",
    help="Data root for output artifacts when using --check-outputs (default: /data)",
  )
  ap.add_argument(
    "--receipts-dir",
    default="repro",
    help="Directory containing *.receipts.json (default: repro)",
  )
  ap.add_argument(
    "--post",
    default="all",
    help="Validate one post number (e.g. 0011) or 'all'",
  )
  ap.add_argument(
    "--check-paths",
    action="store_true",
    help="Verify repo-local artifact paths exist (figures, papers, configs).",
  )
  ap.add_argument(
    "--check-outputs",
    action="store_true",
    help="Also verify output artifacts (blog_artifacts/*, physics/*, etc.) against --data-root. Implies --check-paths.",
  )
  args = ap.parse_args()

  check_outputs = bool(args.check_outputs)
  check_paths = bool(args.check_paths) or check_outputs

  repo_root = Path(args.repo_root).resolve()
  data_root = Path(args.data_root).resolve()
  receipts_dir = (repo_root / args.receipts_dir).resolve()
  if not receipts_dir.exists() and str(args.receipts_dir).startswith("nmoe/"):
    receipts_dir = (repo_root / "community" / str(args.receipts_dir)).resolve()
  if not receipts_dir.exists():
    raise SystemExit(f"missing receipts directory: {receipts_dir}")

  if args.post == "all":
    targets = sorted(path.stem.replace(".receipts", "") for path in receipts_dir.glob("*.receipts.json"))
  else:
    targets = [str(args.post)]
  errors: list[str] = []
  for post in targets:
    p = receipts_dir / f"{post}.receipts.json"
    if not p.exists():
      errors.append(f"missing receipt file: {p}")
      continue
    errors.extend(_verify_receipt(
      p,
      repo_root=repo_root,
      data_root=data_root,
      check_paths=check_paths,
      check_outputs=check_outputs,
    ))

  if errors:
    print(f"verify_post_receipts failed: {len(errors)} issue(s)")
    for e in errors:
      print(f"- {e}")
    return 1

  print("verify_post_receipts passed")
  return 0


if __name__ == "__main__":
  raise SystemExit(main())
